using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using IdentityDoZero.Models;
using IdentityDoZero.Models.ViewModels;
using Microsoft.AspNetCore.Authorization;

namespace IdentityDoZero.Controllers
{
    [Authorize] //Só ira aparecer caso você esteja logado
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;

        public HomeController(ILogger<HomeController> logger)
        {
            _logger = logger;
        }
        [AllowAnonymous] //vai criar uma excessão para mostrar somente esse controller, pq o authorize está em toda controller
        public IActionResult Index()
        {
            return View();
        }
        public IActionResult Privacy()
        {
            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()
